import Foundation

//operators in swift programming
// + operator
var num1: Int = 10
var num2: Int = 20

//arithmetic operators
print(num1 + num2) // + operator
print(num1 - num2) // - operator
print(num1 / num2) // / operator
print(num1 * num2) // * operator
print(num1 % num2) // % operator

//assignment operators
num1 += 10
print(num1)
num1 -= 10
print(num1)
num1 /= 10
print(num1)
num1 *= 10
print(num1)
num1 %= 10
print(num1)

//comparison operators
var number1: Int = 100
var number2: Int = 200
print(number1 == number2)
print(number1 > number2)
print(number1 < number2)
print(number1 >= number2)
print(number1 <= number2)

//logical operators
print()
