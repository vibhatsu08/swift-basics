//Data types in Swift programming
//this is a Int data type
var num: Int
num = 10
print(num)

//this is a floating point data type
var numFloat: Float
numFloat = 4.5
print(numFloat)

//this is a double data type
var numDouble: Double
numDouble = 4.5555
print(numDouble)

//this is a character data type
var charType: Character
charType = "C";
print(charType)

//this is a string data type
var stringType: String
stringType = "hello, world!"
print(stringType)

//this is a boolean data type
var boolType: Bool
boolType = true;
print(boolType)