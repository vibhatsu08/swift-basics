import Foundation

//uppercased function
var string = "hello, world!"
var uppercaseString = string.uppercased()
print(uppercaseString)

//isEmpty function
var isEmptyFunction = string.isEmpty;
print(isEmptyFunction) //returns false, if the string is not empty, and returns true if the string is empty.

//lowercased function
var newString = "HELLO WORLD"
var lowercaseString = newString.lowercased()
print(lowercaseString)


